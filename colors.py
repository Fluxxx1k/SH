def critical(text:str | list[str], negative_letter=2) -> None:
    s = CRITICAL
    for letter in range(len(text)):
        if letter % negative_letter == negative_letter - 1:
            s += NEGATIVE + text[letter] + RESET
        else:
            s += CRITICAL + text[letter]
    s += RESET
    print(s)


